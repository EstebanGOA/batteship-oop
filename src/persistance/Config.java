package persistance;

public class Config {
    private String username;
    private String password;
    private String ip;
    private String port;
    private String database;

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }

    public String getIp() {
        return ip;
    }

    public String getPort() {
        return port;
    }

    public String getDatabase() {
        return database;
    }
}
